package StringManipulation;

public class StringConcept3 {
	public static void main(String[] args) {
		String str = "This is important for eating"; 
		
		System.out.println(str.indexOf("This"));
		System.out.println(str.indexOf("is"));
		System.out.println(str.indexOf("important"));
		System.out.println(str.indexOf("for"));
		System.out.println(str.indexOf("eating"));
	
	}
}
